    @Override
    public void run() {
        try {
            this.pokemonImageLabel.setIcon(new ImageIcon(getClass().getResource(this.pokemonImagePath)));
            Thread.sleep(3000);
            this.evolutionAnimation.setIcon(new ImageIcon(getClass().getResource(URL_GIF)));        
            Thread.sleep(3000);
            this.evolutionAnimation.setVisible(false);
            this.pokemonImageLabel.setIcon(new ImageIcon(getClass().getResource(this.pokemonEvolutionImagePath)));
            Thread.sleep(3000);                        
            evolutionHandler.setChanged(true);
            this.dispose();
        } catch (InterruptedException ex) {
            Logger.getLogger(EvolutionView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

